    private void bValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bValiderActionPerformed
        try {
            //Si un ou plusieurs champs sont vides
            if ((tfCarteGrise.getText().equals("")) || (tfNom.getText().equals("")) || (tfMail.getText().equals("")) || (tfTel.getText().equals("")) || (tfMdp.getText().equals(""))) {
                throw new ExVide();
            }
            cartegrise = Integer.valueOf(tfCarteGrise.getText()); //On récupère son numéro de carte grise et on le convertit en int
            nom = tfNom.getText(); //On récupère ses autres infos dans les composants correspondants
            mail = tfMail.getText();
            tel = tfTel.getText();
            Integer.valueOf(tel); //Pour vérifier si le tel est composé de chiffres sinon ca propage une erreur
            mdp = String.valueOf(tfMdp.getPassword()); //On récupère son nouveau mot de passe et on le convertit en texte
            C.modifierInfos(permis, cartegrise, nom, mail, mdp, tel); //on envoie toutes ses nouvelles infos pour que la fonction les modifie
            JOptionPane.showMessageDialog(this, "Vos modifications ont été enregistrées."); //message de confirmation
        } catch (ExVide ex) { //Si un ou plusieurs champs sont vides
            JOptionPane.showMessageDialog(this, "Tous les champs n'ont pas été remplis.");
        } catch (NumberFormatException ex) { //Si l'utilisateur n'a pas etré que des chiffres pour le téléphone
            JOptionPane.showMessageDialog(this, "Veuillez entrer seulement des chiffres pour le téléphone.");
        }
    }//GEN-LAST:event_bValiderActionPerformed


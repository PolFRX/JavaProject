    public void initCond(Conducteur cond) { //Pour récupérer le conducteur qui va utiliser cette page
        C = cond;
        typeVehic = C.getTypeVehic(); //on affecte toutes ses infos aux variables servant à cela
        nom = C.getNom();
        mail = C.getMail();
        permis = C.getPermis();
        tel = C.getNbtel();
        cartegrise = C.getCartegrise();
        mdp = C.getMdp();
        tfNom.setText(nom); //on affiche ses infos dans les composants correspondants
        tfMail.setText(mail);
        tfCarteGrise.setText("" + cartegrise);
        tfTel.setText(tel);
        tfMdp.setText(mdp);
        if (permis.equals("B")) { //S'il a le permis voiture
            rbPermisB.setSelected(true);
        } else if (permis.equals("moto")) { //S'il a le permis moto
            rbPermisMoto.setSelected(true);
        }
    }

